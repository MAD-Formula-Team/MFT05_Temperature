void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim) {
    if (htim->Instance == TIM2) {
        // Process ADC data and send CAN message every 100 ms
        ADC_Process();
        CAN_Send_Temperature_Data();
        HAL_GPIO_TogglePin(DEBUG_LED_PORT, DEBUG_LED_PIN); // Heartbeat
    } else if (htim->Instance == TIM3) {
        // Periodically check for faults (every 500 ms)
        ADC_Data* data = ADC_GetData();
        for (uint32_t i = 0; i < NUM_THERMISTORS; i++) {
            if (data->fault_flags[i]) {
                fault_detected = 1; // Set global fault flag
                break;
            }
        }
    }
}
